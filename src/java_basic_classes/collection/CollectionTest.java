    @Test
    public void subarraySum() {
        int[] nums = {1, 1,1};
        int k = 2;
        int sum = 0;
        int count = 0;
        for(int i = 0; i < nums.length; i++){
            sum += nums[i];
            if(sum <= k){
                if(sum == k){
                    count++;
                    sum = nums[i];
                }
            }else{
                sum = 0;
                i--;
            }
        }
        System.out.println(count);
    }

